func TestForgejoWebhook_RealWorldDependencyDashboard(t *testing.T) {
	payload := ForgejoEvent{
		Action: "edited",
		Issue: &ForgejoIssue{
			ID:     42,
			Number: 1,
			Title:  "Dependency Dashboard",
			Body:   "# Dependency Dashboard\n\nThis issue lists Renovate updates and detected dependencies.\n\n## Pending Approval\n\nThese branches will be created by Renovate only once you click their checkbox below.\n\n - [x] <!-- approvePr-branch=renovate/golang-1.x -->chore(deps): update dependency golang to v1.23\n\n## Open\n\nThese updates have all been created already.\n\n - [ ] <!-- rebase-all-open-prs -->**Click on this checkbox to rebase all open PRs at once**\n",
		},
		Repository: ForgejoRepository{
			ID:       500,
			Name:     "renovate-config",
			FullName: "example/renovate-config",
		},
	}

	updateCalled := false
	mockManager := &mockWebhookManager{
		updateProjectStatusFunc: func(ctx context.Context, project string, jobId crdmanager.RenovateJobIdentifier, status api.RenovateProjectStatus) error {
			updateCalled = true
			if project != "example/renovate-config" {
				t.Errorf("expected project example/renovate-config, got %s", project)
			}
			return nil
		},
	}

	server := &Server{
		manager: mockManager,
		logger:  logr.Discard(),
	}

	body, _ := json.Marshal(payload)

	req := httptest.NewRequest(http.MethodPost, "/webhook/v1/forgejo?namespace=renovate-operator&job=codefloe", bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("X-Forgejo-Event", "issues")

	w := httptest.NewRecorder()
	server.forgejoWebhook(w, req)

	if w.Code != http.StatusAccepted {
		t.Errorf("expected status %d, got %d", http.StatusAccepted, w.Code)
	}

	var response map[string]string
	json.Unmarshal(w.Body.Bytes(), &response)
	if response["message"] != "renovate job scheduled" {
		t.Errorf("expected message 'renovate job scheduled', got %q", response["message"])
	}

	if !updateCalled {
		t.Error("expected UpdateProjectStatus to be called")
	}
}
